function asignar(tick_id){
    $.post("../../controller/ticket.php?op=mostrar", {tick_id: tick_id}, function (data) {
        data =JSON.parse(data);

        $('#tick_id'). val(data.tick_id);

        $('#mdltitulo').html('Asignar agente');
        $("#modalasignar").modal('show');
    });
}

